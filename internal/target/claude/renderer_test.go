func TestRenderMergesSettingsJSON(t *testing.T) {
	workDir := t.TempDir()
	settingsPath := filepath.Join(workDir, ".claude", "settings.json")
	if err := os.MkdirAll(filepath.Dir(settingsPath), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(settingsPath, []byte(`{"custom":true,"model":"old"}`), 0o644); err != nil {
		t.Fatal(err)
	}
	plan := renderPlan(t, target.RenderInput{
		Flow:    testFlow(),
		Models:  binding.Models{"main": "gpt"},
		Scope:   binding.ScopeProject,
		WorkDir: workDir,
		HomeDir: t.TempDir(),
	})
	var settingsAction install.Action
	for _, action := range plan.Actions {
		if action.Path == settingsPath {
			settingsAction = action
			break
		}
	}
	if settingsAction.Path == "" {
		t.Fatalf("settings action not found")
	}
	if settingsAction.Kind != install.ActionUpdate {
		t.Fatalf("settings action kind = %q", settingsAction.Kind)
	}
	var settings map[string]any
	if err := json.Unmarshal(settingsAction.Content, &settings); err != nil {
		t.Fatal(err)
	}
	if settings["custom"] != true {
		t.Fatalf("custom setting was not preserved: %#v", settings)
	}
	if settings["model"] != "gpt" {
		t.Fatalf("model = %#v", settings["model"])
	}
	if settings["alwaysThinkingEnabled"] != true {
		t.Fatalf("alwaysThinkingEnabled = %#v", settings["alwaysThinkingEnabled"])
	}
	features, ok := settings["features"].(map[string]any)
	if !ok || features["multiAgent"] != true {
		t.Fatalf("features = %#v", settings["features"])
	}
}

