func TestRenderOmitsGeneratedMarker(t *testing.T) {
	workDir := t.TempDir()
	plan := renderPlan(t, target.RenderInput{
		Flow:    testFlow(),
		Models:  binding.Models{"main": "gpt"},
		Scope:   binding.ScopeProject,
		WorkDir: workDir,
		HomeDir: t.TempDir(),
	})
	for _, action := range plan.Actions {
		if strings.Contains(string(action.Content), "Generated by agentsflow") {
			t.Fatalf("action %s contains generated marker:\n%s", action.Path, action.Content)
		}
	}
}

func TestRenderProjectInstructionsInlineCLAUDE(t *testing.T) {
	workDir := t.TempDir()
	plan := renderPlan(t, target.RenderInput{
		Flow:    testFlow(),
		Models:  binding.Models{"main": "gpt"},
		Scope:   binding.ScopeProject,
		WorkDir: workDir,
		HomeDir: t.TempDir(),
	})
	claudePath := filepath.Join(workDir, "CLAUDE.md")
	assertActionKind(t, plan, filepath.Join(workDir, ".claude", "agents"), install.ActionCleanDir)
	if content := actionContent(t, plan, claudePath); content != "# Test" {
		t.Fatalf("CLAUDE.md = %q, want inline instructions", content)
	}
	settingsPath := filepath.Join(workDir, ".claude", "settings.json")
	settings := actionContent(t, plan, settingsPath)
	for _, want := range []string{
		`"alwaysThinkingEnabled": true`,
		`"model": "gpt"`,
		`"multiAgent": true`,
	} {
		if !strings.Contains(settings, want) {
			t.Fatalf("settings.json missing %q:\n%s", want, settings)
		}
	}
	agent := actionContent(t, plan, filepath.Join(workDir, ".claude", "agents", "reviewer.md"))
	for _, want := range []string{
		"name: reviewer",
		"description: Reviews",
		"model: gpt",
		"effort: medium",
		"Review.",
	} {
		if !strings.Contains(agent, want) {
			t.Fatalf("reviewer.md missing %q:\n%s", want, agent)
		}
	}
	for _, action := range plan.Actions {
		if action.Path == filepath.Join(workDir, "AGENTS.md") {
			t.Fatalf("unexpected AGENTS.md action: %#v", action)
		}
	}
}

