func renderPlan(t *testing.T, input target.RenderInput) install.Plan {
	t.Helper()
	artifacts, diags := (Renderer{}).Render(context.Background(), input)
	if len(diags) != 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}
	return install.BuildPlan(artifacts)
}

func actionContent(t *testing.T, plan install.Plan, path string) string {
	t.Helper()
	for _, action := range plan.Actions {
		if action.Path == path {
			return string(action.Content)
		}
	}
	t.Fatalf("expected action for %s, got %#v", path, plan.Actions)
	return ""
}

