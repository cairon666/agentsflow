func TestRenderCodexAgentUsesMultilineDeveloperInstructions(t *testing.T) {
	workDir := t.TempDir()
	flow := testFlow()
	agent := flow.Agents["reviewer"]
	agent.Prompt = "Line one.\n\n- Line two\n- Line three\n"
	flow.Agents["reviewer"] = agent
	plan := renderPlan(t, target.RenderInput{
		Flow:    flow,
		Models:  binding.Models{"main": "gpt-test"},
		Scope:   binding.ScopeProject,
		WorkDir: workDir,
		HomeDir: t.TempDir(),
	})
	content := actionContent(t, plan, filepath.Join(workDir, ".codex", "agents", "reviewer.toml"))
	if !strings.Contains(content, "developer_instructions = \"\"\"\nLine one.\n\n- Line two\n- Line three\n\"\"\"") {
		t.Fatalf("developer_instructions was not rendered as multiline TOML:\n%s", content)
	}
	var parsed map[string]any
	if err := toml.Unmarshal([]byte(content), &parsed); err != nil {
		t.Fatal(err)
	}
	if parsed["developer_instructions"] != agent.Prompt {
		t.Fatalf("developer_instructions = %#v, want %#v", parsed["developer_instructions"], agent.Prompt)
	}
}

func TestRenderCodexAgentMapsWebSearchCapability(t *testing.T) {
	workDir := t.TempDir()
	flow := testFlow()
	flow.PermissionProfiles["docs"] = flowmodel.PermissionProfile{
		Capabilities: map[string]string{
			"edit_files": "deny",
			"web_search": "allow",
		},
	}
	flow.Agents["docs-researcher"] = flowmodel.Agent{
		ID:                "docs-researcher",
		Description:       "Researches docs",
		ModelSlot:         "main",
		ReasoningEffort:   "medium",
		PermissionProfile: "docs",
		Prompt:            "Research.",
	}

	plan := renderPlan(t, target.RenderInput{
		Flow:    flow,
		Models:  binding.Models{"main": "gpt-test"},
		Scope:   binding.ScopeProject,
		WorkDir: workDir,
		HomeDir: t.TempDir(),
	})

	docs := actionContent(t, plan, filepath.Join(workDir, ".codex", "agents", "docs-researcher.toml"))
	if !strings.Contains(docs, "web_search = 'live'") {
		t.Fatalf("expected docs-researcher to enable live web_search, got:\n%s", docs)
	}

	reviewer := actionContent(t, plan, filepath.Join(workDir, ".codex", "agents", "reviewer.toml"))
	if !strings.Contains(reviewer, "web_search = 'disabled'") {
		t.Fatalf("expected reviewer to disable web_search, got:\n%s", reviewer)
	}
}

func TestRenderMergesConfigTOML(t *testing.T) {
	workDir := t.TempDir()
	configPath := filepath.Join(workDir, ".codex", "config.toml")
	if err := os.MkdirAll(filepath.Dir(configPath), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(configPath, []byte(`
custom = "keep"
model = "old"

[features]
existing_feature = true

[agents]
existing_limit = 3
`), 0o644); err != nil {
		t.Fatal(err)
	}

	plan := renderPlan(t, target.RenderInput{
		Flow:    testFlow(),
		Models:  binding.Models{"main": "gpt"},
		Scope:   binding.ScopeProject,
		WorkDir: workDir,
		HomeDir: t.TempDir(),
	})

	var configAction install.Action
	for _, action := range plan.Actions {
		if action.Path == configPath {
			configAction = action
			break
		}
	}
	if configAction.Path == "" {
		t.Fatalf("config action not found")
	}
	if configAction.Kind != install.ActionUpdate {
		t.Fatalf("config action kind = %q", configAction.Kind)
	}

	config := string(configAction.Content)
	for _, want := range []string{
		"custom = 'keep'",
		"model = 'gpt'",
		"existing_feature = true",
		"multi_agent = true",
		"existing_limit = 3",
		"max_threads = 7",
		"max_depth = 2",
	} {
		if !strings.Contains(config, want) {
			t.Fatalf("expected config.toml to contain %q, got:\n%s", want, config)
		}
	}
}

